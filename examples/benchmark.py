"""
examples/benchmark.py
FEM-BEM MAP vs Kaiser-Squires systematic benchmark.

Both methods receive shear generated by the FEM-BEM forward model.
KS has the structural advantage of using a periodic FFT kernel but
lacks inpainting and correct boundary conditions.

Run:
    python examples/benchmark.py
"""

import sys, os, time
import numpy as np
from scipy.interpolate import griddata
sys.path.insert(0, os.path.join(os.path.dirname(__file__), ".."))

from femmi.operators import build_operators
from femmi.forward   import DifferentiableForward
from femmi.inverse   import MAPReconstructor

try:
    from smpy.config import Config
    from smpy.mapping_methods.kaiser_squires.kaiser_squires import KaiserSquiresMapper
    HAS_SMPY = True
except ImportError:
    HAS_SMPY = False
    print("WARNING: SMPy not found. KS comparison columns will be N/A.")

PASS = "PASS"
FAIL = "FAIL"
results = []

def record(name, ok, detail=""):
    tag = PASS if ok else FAIL
    print(f"  {tag}  {name}")
    if detail:
        print(f"       {detail}")
    if True:
        results.append((name, ok))


NX_FEM  = 20
NX_GRID = 64
DOMAIN  = (-2.0, 2.0, -2.0, 2.0)
xmin, xmax, ymin, ymax = DOMAIN
SIGMA_LENS = 0.5

print(f"Building FEM mesh ({NX_FEM}x{NX_FEM})...")
t0  = time.perf_counter()
ops = build_operators(NX_FEM, NX_FEM,
                      xmin=xmin, xmax=xmax, ymin=ymin, ymax=ymax,
                      verbose=False)
print(f"  {ops.n_nodes} nodes  ({time.perf_counter()-t0:.1f}s)")
fem_nodes = np.array(ops.mesh.nodes)

xi_grid = np.linspace(xmin, xmax, NX_GRID)
yi_grid = np.linspace(ymin, ymax, NX_GRID)
XX, YY  = np.meshgrid(xi_grid, yi_grid)
grid_pts = np.column_stack([XX.ravel(), YY.ravel()])


def gaussian_kappa_grid(cx=0.0, cy=0.0, sigma=SIGMA_LENS, A=1.0):
    return A * np.exp(-((XX-cx)**2 + (YY-cy)**2) / (2*sigma**2))


def double_gaussian_grid():
    k1 = np.exp(-((XX+0.7)**2 + YY**2) / (2*0.3**2))
    k2 = 0.7 * np.exp(-((XX-0.7)**2 + YY**2) / (2*0.4**2))
    return k1 + k2


def run_ks(g1_grid, g2_grid, smoothing_sigma=1.0, sky_fraction=0.15):
    cfg = Config.from_defaults("kaiser_squires").to_dict()
    cfg["methods"]["kaiser_squires"]["smoothing"]["sigma"] = smoothing_sigma
    kappa_raw, _ = KaiserSquiresMapper(cfg).create_maps(g1_grid, g2_grid)
    ny, nx = kappa_raw.shape
    m   = max(1, int(sky_fraction * min(ny, nx)))
    sky = np.concatenate([kappa_raw[:m,:].ravel(), kappa_raw[-m:,:].ravel(),
                          kappa_raw[:,:m].ravel(), kappa_raw[:,-m:].ravel()])
    return kappa_raw - sky.mean()


def run_map(g1_grid, g2_grid, lam_reg=1e-2, wiener_length=0.5,
            maxiter=400, mask_grid=None):
    g1_fem = griddata(grid_pts, g1_grid.ravel(), fem_nodes, method='linear', fill_value=0.0)
    g2_fem = griddata(grid_pts, g2_grid.ravel(), fem_nodes, method='linear', fill_value=0.0)
    if mask_grid is not None:
        mf = griddata(grid_pts, mask_grid.ravel().astype(float), fem_nodes, method='nearest') > 0.5
        g1_fem[mf] = 0.0; g2_fem[mf] = 0.0

    fwd = DifferentiableForward(ops, lam_reg=lam_reg)
    rec = MAPReconstructor(fwd, maxiter=maxiter, gtol=1e-7,
                           wiener_length=wiener_length, callback_every=0)
    kmap_nodes, _ = rec.reconstruct(g1_fem, g2_fem, verbose=False)

    kmap_grid = griddata(fem_nodes, kmap_nodes, (XX, YY), method='linear')
    kmap_nn   = griddata(fem_nodes, kmap_nodes, (XX, YY), method='nearest')
    return np.where(np.isfinite(kmap_grid), kmap_grid, kmap_nn)


def l2(pred, truth, deep_lim=1.5):
    mask = (np.abs(XX) < deep_lim) & (np.abs(YY) < deep_lim)
    p = pred[mask]; t = truth[mask]
    d = np.linalg.norm(t)
    return float(np.linalg.norm(p - t) / (d if d > 0 else 1.0))


rng = np.random.default_rng(42)


def run_case(name, kappa_true, noise_frac, lam_reg=1e-2, wiener_length=0.5,
             smoothing_sigma=1.0, mask_grid=None, pass_threshold_ratio=1.1, maxiter=400):
    print(f"\n  [{name}]")

    kappa_fem  = griddata(grid_pts, kappa_true.ravel(), fem_nodes, method='linear', fill_value=0.0)
    g1_fem_t, g2_fem_t = ops.forward(kappa_fem)

    def _interp(v):
        g = griddata(fem_nodes, v, (XX, YY), method='linear', fill_value=0.0)
        n = griddata(fem_nodes, v, (XX, YY), method='nearest')
        return np.where(np.isfinite(g), g, n)

    g1_true = _interp(g1_fem_t); g2_true = _interp(g2_fem_t)
    noise_s = noise_frac * np.std(np.hypot(g1_true, g2_true))
    g1_obs  = g1_true + rng.normal(0, noise_s, g1_true.shape)
    g2_obs  = g2_true + rng.normal(0, noise_s, g2_true.shape)

    if mask_grid is not None:
        g1_obs = np.where(mask_grid, 0.0, g1_obs)
        g2_obs = np.where(mask_grid, 0.0, g2_obs)

    t0   = time.perf_counter()
    kmap = run_map(g1_obs, g2_obs, lam_reg=lam_reg, wiener_length=wiener_length,
                   mask_grid=mask_grid, maxiter=maxiter)
    t_map = time.perf_counter() - t0
    l2_map = l2(kmap, kappa_true)
    print(f"    MAP L2={l2_map:.4f}  ({t_map:.1f}s)")

    l2_ks = np.nan
    if HAS_SMPY:
        t0    = time.perf_counter()
        kks   = run_ks(g1_obs, g2_obs, smoothing_sigma=smoothing_sigma)
        t_ks  = time.perf_counter() - t0
        l2_ks = l2(kks, kappa_true)
        print(f"    KS  L2={l2_ks:.4f}  ({t_ks:.1f}s)")
        improv = (l2_ks - l2_map) / l2_ks * 100
        print(f"    improvement: {improv:+.1f}%")
        ok = l2_map <= pass_threshold_ratio * l2_ks
        record(f"{name}: MAP L2 <= {pass_threshold_ratio}x KS L2", ok,
               f"MAP={l2_map:.4f}  KS={l2_ks:.4f}  ratio={l2_map/l2_ks:.2f}")
        return l2_map, l2_ks, improv
    else:
        record(f"{name}: MAP converged", l2_map < 0.8, f"MAP L2={l2_map:.4f}")
        return l2_map, np.nan, np.nan


k_gauss = gaussian_kappa_grid()

case_results = {}
case_results["noiseless"]  = run_case("1. Noiseless", k_gauss, 0.0, lam_reg=1e-5,
                                       smoothing_sigma=0.5, maxiter=600, pass_threshold_ratio=1.5)
case_results["10pct"]      = run_case("2. 10% noise", k_gauss, 0.10, lam_reg=1e-2)
case_results["20pct"]      = run_case("3. 20% noise", k_gauss, 0.20, lam_reg=3e-2, smoothing_sigma=1.5)
mask = (XX**2 + YY**2) < 0.6**2
case_results["masked"]     = run_case("4. Central mask r<0.6, 10% noise", k_gauss, 0.10,
                                       lam_reg=2e-2, mask_grid=mask, pass_threshold_ratio=1.0)
k_off = gaussian_kappa_grid(cx=0.8, cy=0.5)
case_results["offcenter"]  = run_case("5. Off-centre lens (0.8, 0.5), 10% noise", k_off, 0.10)
k_two = double_gaussian_grid()
case_results["two_comp"]   = run_case("6. Two-component lens, 10% noise", k_two, 0.10)


if __name__ == "__main__":
    print(f"\nPass/Fail:")
    n_pass = sum(1 for _, ok in results if ok)
    n_fail = sum(1 for _, ok in results if not ok)
    for name, ok in results:
        print(f"  {'ok' if ok else 'FAIL'}  {name}")
    print(f"\n  {n_pass}/{n_pass+n_fail} passed")
    if not HAS_SMPY:
        print("  NOTE: Install SMPy to enable full FEM-BEM vs KS comparison.")

    import sys; sys.exit(0 if n_fail == 0 else 1)